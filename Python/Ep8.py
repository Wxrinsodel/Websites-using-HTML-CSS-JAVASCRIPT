# print ('ผลลบ' , x-y ) จะได้คำตอบที่ถูก 
# ('ผลบวก' , x+y) ('ผลลบ' , x-y) ('ผลคูณ' , x*y)('ผลหาร' , x/y)('ผลหารปัดเศษ' , x//y)('ยกกำลัง' , x**y)('หารเอาเศษ' , x%y)
x = 10
y = 4

print('ผลบวก' , x+y)
print('ผลลบ' , x-y)
print('ผลคูณ' , x*y)
print('ผลหาร' , x/y)
print('ผลหารปัดเศษ' , x//y)
print('ยกกำลัง' , x**y)
print('หารเอาเศษ' , x%y)